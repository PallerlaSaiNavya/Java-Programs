public class HungarianNotationExample {
    public static void main(String[] args) {
        // Example variables with Hungarian notation
        String strName = "John";
        int intAge = 30;
        boolean boolIsStudent = true;
        double dblGpa = 3.5;

        // Output the values
        System.out.println("Name: " + strName);
        System.out.println("Age: " + intAge);
        System.out.println("Is Student: " + boolIsStudent);
        System.out.println("GPA: " + dblGpa);
    }
}
